package com.example.uilistviewtest;

public class FruitModel {

	private String name;

	private int imageId;

	public FruitModel(String name, int imageId) {
		this.name = name;
		this.imageId = imageId;
	}

	public String getName() {
		return name;
	}

	public int getImageId() {
		return imageId;
	}

}
